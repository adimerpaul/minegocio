/// País con su nombre, código ISO-3166-1 alpha-2 (para la bandera emoji) y
/// prefijo telefónico internacional.
class Pais {
  final String nombre;
  final String iso;
  final String codigo;

  const Pais({
    required this.nombre,
    required this.iso,
    required this.codigo,
  });

  /// Bandera emoji generada a partir del código ISO de dos letras.
  String get bandera {
    const base = 0x1F1E6 - 65; // 'A' -> U+1F1E6 (regional indicator A)
    return String.fromCharCodes(
      iso.toUpperCase().codeUnits.map((u) => base + u),
    );
  }

  /// Texto compacto para mostrar en el selector: "🇧🇴 Bolivia (+591)".
  String get etiqueta => '$bandera $nombre ($codigo)';

  /// Texto corto para chips: "🇧🇴 +591".
  String get corto => '$bandera $codigo';
}

