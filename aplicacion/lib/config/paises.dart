/// Busca un país por su código numérico (ej. `+591`).
Pais? paisPorCodigo(String? codigo) {
  if (codigo == null || codigo.isEmpty) return null;
  final limpio = codigo.trim();
  return paises.cast<Pais?>().firstWhere(
        (p) => p!.codigo == limpio,
        orElse: () => null,
      );
}
